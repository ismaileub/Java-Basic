public class DataType {

     public static void main(String[] args) {
        boolean b = true;
        System.out.println("b = "+b);

        float f = 10.23f;
        System.out.println("f = "+f);

        double d = 1233.2336;
        System.out.printf("d = %f\n",d);

        char c = 'a';
        System.out.printf("c = %c\n",c);
     }
}